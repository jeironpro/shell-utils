package exercicis;
public class Duplica {
    public static void main(String[] args) {
        // declara les variable a utilitzar
        int nombreADuplicar;
        int nombreDuplicat;
        
        // obté el nombre a duplicar
        nombreADuplicar = 5;
        
        // calcula el doble
        nombreDuplicat = nombreADuplicar * 2;
        
        // mostra resultats
        System.out.println("El doble de " + nombreADuplicar + " és " + nombreDuplicat);
    }
}
